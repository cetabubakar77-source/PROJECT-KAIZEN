// Function to render curriculum view
function renderCurriculumView() {
    const curriculumView = document.getElementById('curriculum-view');
    if (!curriculumView) return;

    let html = `
        <div class="curriculum-container">
    `;

    for (const [subject, data] of Object.entries(curriculum10MH)) {
        html += `
            <div class="curriculum-subject-card">
                <div class="subject-header" onclick="toggleSubjectTopics('${subject}', this)">
                    <div style="display: flex; align-items: center; gap: 12px;">
                        <div class="subject-icon-small" style="color: ${data.color}; background: ${data.color}20;">
                            <i class="ph ${data.icon}"></i>
                        </div>
                        <div>
                            <h3>${subject}</h3>
                        </div>
                    </div>
                    <i class="ph ph-caret-down chevron" style="transition: transform 0.3s;"></i>
                </div>
                <div class="topics-list" style="display: none; max-height: 0; overflow: hidden; transition: max-height 0.3s ease-out;">
        `;

        data.topics.forEach((topic, idx) => {
            const importantClass = topic.important ? 'important-topic' : '';
            html += `
                <div class="topic-item ${importantClass}">
                    <div class="topic-content">
                        <div>
                            <p class="topic-name">${topic.name}</p>
                            <p class="topic-description">${topic.description}</p>
                        </div>
                        <span class="marks-badge">${topic.marks} marks</span>
                    </div>
                </div>
            `;
        });

        html += `
                </div>
            </div>
        `;
    }

    html += `</div>`;
    curriculumView.innerHTML = html;
}

// Toggle subject topics
function toggleSubjectTopics(subject, element) {
    const card = element.closest('.curriculum-subject-card');
    const topicsList = card.querySelector('.topics-list');
    const chevron = element.querySelector('.chevron');
    
    const isOpen = topicsList.style.display === 'block';
    
    if (isOpen) {
        topicsList.style.display = 'none';
        topicsList.style.maxHeight = '0';
        chevron.style.transform = 'rotate(0deg)';
    } else {
        topicsList.style.display = 'block';
        topicsList.style.maxHeight = topicsList.scrollHeight + 'px';
        chevron.style.transform = 'rotate(180deg)';
    }
}

// Export for use
window.curriculum10MH = curriculum10MH;
window.renderCurriculumView = renderCurriculumView;
window.toggleSubjectTopics = toggleSubjectTopics;
